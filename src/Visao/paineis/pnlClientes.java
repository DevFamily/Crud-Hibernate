private void salvarClientes(){
        clientes.setNome(this.txtNomeCliente.getText());
        clientes.setMorada(this.txtMoradaCliente.getText());
        clientes.setContacto(Integer.parseInt(this.txtContactoCliente.getText()));
        clientes.setEmail(this.txtEmailCliente.getText());        
       if (controleClientes.salvarClienteController(clientes)>0){
           JOptionPane.showMessageDialog(this,"Produto salvo com sucesso! ");

           this.carregarClientes();
           this.clean();
           this.habilitarCampos(false);
           
       }else{
           JOptionPane.showMessageDialog(this,"Erro ao salvar Produto! ");
           
       }
    
}
private void alterarClientes(){
        clientes.setNome(this.txtNomeCliente.getText());
        clientes.setMorada(this.txtMoradaCliente.getText());
        clientes.setContacto(Integer.parseInt(this.txtContactoCliente.getText()));
        clientes.setEmail(this.txtEmailCliente.getText());
        
       if (controleClientes.atualizarClienteController(clientes)){
           JOptionPane.showMessageDialog(this,"Produto alterado com sucesso! ");

           this.carregarClientes();
           this.clean();
           this.habilitarCampos(false);
           
       }else{
           JOptionPane.showMessageDialog(this,"Erro ao alterar Produto! ");
           
       }
    
}

     public  void carregarClientes(){
        listaCliente = controleClientes.getListaClienteController();
        DefaultTableModel modelo = (DefaultTableModel)tblCliente.getModel();
        modelo.setNumRows(0);
        //inserir clientes na tabelaa
        int cont = listaCliente.size();
        for (int i=0; i<cont; i++){
            modelo.addRow(new Object[]{
            listaCliente.get(i).getIdCliente(),
            listaCliente.get(i).getNome(),
            listaCliente.get(i).getMorada(),
            listaCliente.get(i).getContacto(),
            listaCliente.get(i).getEmail(),
        });
        }
        
    }
      
